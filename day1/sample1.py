# یادگیری کار با متغیرها
# how to work with variables

# variable -> {name, type, value}

# variable definition (or declaration)
# تعریف متغیر
x: int # integer
y: int; a: str # string

# variable assignment
# مقداردهی متغیرها
x = 3          # numeric literal
y = 12         # numeric literal
a = "arezoo"   # string literal

if ((y / x) == 4):  # اگر بلی
     print(a)       # یعنی مقدار متغیر را بنویس
else:               # اگر نه
     a = "azita"

print(a)
